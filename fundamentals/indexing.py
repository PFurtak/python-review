my_string = "hello world"

# indexing
print(my_string)
print(my_string[2])
print(my_string[7])
print(my_string[-1])
print(my_string[-3])

# slicing
print(my_string[2:])
print(my_string[:3])
print(my_string[3:6])
print(my_string[6:])
print(my_string[:5])

# slicing with step size
print(my_string[::2])
print(my_string[:5:2])
print(my_string[:-3:4])

# reverse string trick with slicing
print(my_string[::-1])
